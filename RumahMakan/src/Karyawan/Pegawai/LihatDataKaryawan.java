    private void tampilkanDataKaryawan() {
    String username = UserSession.getLoggedInUsername(); 

    if (username != null && !username.isEmpty()) {
        try {
            Connection conn = DatabaseConnection.connect();
            String sql = "SELECT * FROM karyawan WHERE username = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, username);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                jTextField1.setText(String.valueOf(rs.getInt("id_karyawan")));
                jTextField2.setText(rs.getString("nik"));
                jTextField3.setText(rs.getString("nama_karyawan"));
                jComboBox1.setSelectedItem(rs.getString("jabatan"));
                jTextField5.setText(rs.getString("no_hp"));
                jTextField6.setText(rs.getString("alamat"));
                jTextField7.setText(rs.getString("jenis_kelamin"));
                jTextField8.setText(rs.getString("email"));
                jTextField9.setText(rs.getString("username"));

                // Optional: Buat readonly
                jTextField1.setEditable(false);
                jTextField2.setEditable(false);
                jTextField3.setEditable(false);
                jComboBox1.setEnabled(false);
                jTextField5.setEditable(false);
                jTextField6.setEditable(false);
                jTextField7.setEditable(false);
                jTextField8.setEditable(false);
                jTextField9.setEditable(false);
            } else {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan untuk username: " + username);
            }

            rs.close();
            ps.close();
            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Kesalahan database: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(this, "User belum login.");
    }
}
